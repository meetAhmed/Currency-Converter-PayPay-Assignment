import Foundation

/**
 This is a simple struct representing country information.
 
 It contains two properties:
 - 'name': A string representing the name of the country.
 - 'code': A string representing the ISO code of the country.
 
 The struct is used to store country information in the 'CountryFlagHelper' class.
 */
struct CountryInfo {
    let name: String
    let code: String
}
